// Arrow function

/*
    Para usarmos uma arrow function, ao inves de digitarmos:
    ************************************************************
    *         const sayMyName = function () {                  *
    *               console.log('Elivan')                      *
    *         }                                                *
    ************************************************************
    Colocamos uma setinha => depois do parametro passado para a função.

    Esse é um jeito mas curto de escrever uma função.
    */
    
const sayMyName = () => {
    console.log('Elivan')
}